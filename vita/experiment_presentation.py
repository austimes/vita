"""Generate a narrative HTML explainer for a Vita experiment."""

# ruff: noqa: E501

from __future__ import annotations

import json
from html import escape
from pathlib import Path
from typing import Any


def generate_presentation(
    experiment_dir: Path,
    summary: dict | None = None,
    interpretation: dict | None = None,
    brief: dict | None = None,
) -> Path:
    """Generate ``presentation/index.html`` for an experiment directory."""
    experiment_dir = experiment_dir.expanduser().resolve()

    if summary is None:
        summary_path = experiment_dir / "conclusions" / "summary.json"
        summary = json.loads(summary_path.read_text(encoding="utf-8"))

    if interpretation is None:
        interpretation = _load_optional_json(
            experiment_dir / "conclusions" / "interpretation.json"
        )

    if brief is None:
        brief = _load_optional_json(experiment_dir / "planning" / "brief.json")

    html = _render_html(summary, brief=brief, interpretation=interpretation)

    out_dir = experiment_dir / "presentation"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / "index.html"
    out_path.write_text(html, encoding="utf-8")
    return out_path


def _load_optional_json(path: Path) -> dict | None:
    if not path.exists():
        return None
    return json.loads(path.read_text(encoding="utf-8"))


def _render_html(
    summary: dict,
    *,
    brief: dict | None,
    interpretation: dict | None,
) -> str:
    """Render a single self-contained narrative explainer page."""
    view = _build_view_model(summary, brief=brief, interpretation=interpretation)
    sections = [
        _render_hero(view),
        _render_short_answer(view),
        _render_design(view),
        _render_expected_mechanisms(view),
        _render_what_happened(view),
        _render_reasoning(view),
        _render_question_answers(view),
        _render_synthesis(view),
        _render_appendix(view),
    ]
    body = "\n".join(section for section in sections if section)

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>{escape(view["title"])} - Vita Experiment</title>
  <link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link href="https://fonts.googleapis.com/css2?family=Instrument+Serif:ital@0;1&family=IBM+Plex+Mono:wght@400;500&display=swap" rel="stylesheet">
  <style>{_css()}</style>
</head>
<body>
  <main>
{body}
  </main>
  <footer>
    <p>Generated by Vita</p>
    <p>{escape(view["generated_at"])}</p>
  </footer>
</body>
</html>"""


def _build_view_model(
    summary: dict,
    *,
    brief: dict | None,
    interpretation: dict | None,
) -> dict[str, Any]:
    methodology = summary.get("methodology", {})
    runs = list(summary.get("runs", []))
    comparisons = [_enrich_comparison(c) for c in summary.get("comparisons", [])]
    evidence_index = list((interpretation or {}).get("evidence_index", []))
    evidence_by_id = {
        item["id"]: item for item in evidence_index if isinstance(item.get("id"), str)
    }
    baseline_objective = _baseline_objective(summary)
    comparison_deltas = [
        abs(float(comp["objective_delta"]))
        for comp in comparisons
        if isinstance(comp.get("objective_delta"), (int, float))
    ]
    run_objectives = [
        float(run["objective"])
        for run in runs
        if isinstance(run.get("objective"), (int, float))
    ]

    question = ""
    if interpretation:
        question = str(interpretation.get("research_question", "")).strip()
    if not question and brief:
        question = str(brief.get("research", {}).get("question", "")).strip()

    scope = ""
    if brief:
        scope = str(brief.get("research", {}).get("scope", "")).strip()

    generated_at = (
        summary.get("summarized_at")
        or summary.get("concluded_at")
        or summary.get("completed_at")
        or ""
    )

    return {
        "title": str(summary.get("experiment_id", "Experiment")),
        "summary": summary,
        "brief": brief,
        "interpretation": interpretation,
        "question": question,
        "scope": scope,
        "generated_at": str(generated_at),
        "status": str(summary.get("status", "unknown")),
        "methodology": methodology,
        "runs": runs,
        "comparisons": comparisons,
        "evidence_index": evidence_index,
        "evidence_by_id": evidence_by_id,
        "baseline_objective": baseline_objective,
        "largest_objective_delta": max(comparison_deltas, default=None),
        "run_count": len(runs),
        "comparison_count": len(comparisons),
        "max_run_objective": max(run_objectives, default=None),
    }


def _enrich_comparison(comparison: dict) -> dict:
    capacity_deltas = list(comparison.get("capacity_deltas", []))
    ranked = sorted(
        capacity_deltas,
        key=lambda item: abs(float(item.get("delta", 0.0))),
        reverse=True,
    )
    largest_shift = ranked[0] if ranked else None
    enriched = dict(comparison)
    enriched["capacity_deltas"] = capacity_deltas
    enriched["largest_capacity_shift"] = largest_shift
    return enriched


