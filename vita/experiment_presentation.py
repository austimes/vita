"""Generate self-contained HTML presentation for a Vita experiment."""

from __future__ import annotations

import json
from html import escape
from pathlib import Path
from typing import Any


def generate_presentation(
    experiment_dir: Path,
    summary: dict | None = None,
    interpretation: dict | None = None,
    brief: dict | None = None,
) -> Path:
    """Generate presentation/index.html from conclusions/summary.json.

    If *summary* dict is provided, use it directly.
    Otherwise, load from ``conclusions/summary.json``.

    *interpretation* and *brief* are optional enrichments loaded from
    ``conclusions/interpretation.json`` and ``planning/brief.json``
    respectively.  When not supplied the function attempts to load them
    from disk but degrades gracefully if the files are absent.

    Returns path to generated ``index.html``.
    """
    if summary is None:
        summary_path = experiment_dir / "conclusions" / "summary.json"
        summary = json.loads(summary_path.read_text(encoding="utf-8"))

    if interpretation is None:
        interp_path = experiment_dir / "conclusions" / "interpretation.json"
        if interp_path.exists():
            interpretation = json.loads(interp_path.read_text(encoding="utf-8"))

    if brief is None:
        brief_path = experiment_dir / "planning" / "brief.json"
        if brief_path.exists():
            brief = json.loads(brief_path.read_text(encoding="utf-8"))

    html = _render_html(summary, interpretation=interpretation, brief=brief)

    out_dir = experiment_dir / "presentation"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / "index.html"
    out_path.write_text(html, encoding="utf-8")
    return out_path


# ---------------------------------------------------------------------------
# Top-level renderer
# ---------------------------------------------------------------------------


def _render_html(
    summary: dict,
    interpretation: dict | None = None,
    brief: dict | None = None,
) -> str:
    """Render complete HTML document."""
    sections = [
        _render_header(summary),
        _render_question(summary, interpretation),
        _render_brief_section(brief) if brief else "",
        _render_methodology(summary),
        _render_results(summary),
        _render_comparisons(summary),
        # --- Agent interpretation (visually separated) ---
        _render_interpretation_section(interpretation) if interpretation else "",
        (
            _render_question_answers(interpretation)
            if interpretation
            else _render_answers(summary)
        ),
        _render_findings(summary),
        _render_hypothesis_outcomes(summary),
        _render_surprises(summary),
        _render_limitations(summary),
    ]
    body = "\n".join(s for s in sections if s)
    concluded = escape(str(summary.get("concluded_at", "")))
    title = escape(str(summary.get("experiment_id", "Experiment")))

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{title} — Vita Experiment</title>
    <style>{_css()}</style>
</head>
<body>
{body}
    <footer>Generated by Vita · {concluded}</footer>
</body>
</html>"""


