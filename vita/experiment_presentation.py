def _render_hero(view: dict[str, Any]) -> str:
    question_html = ""
    if view["question"]:
        question_html = f'<p class="question">{escape(view["question"])}</p>'

    scope_html = ""
    if view["scope"]:
        scope_html = f'<p class="lede">{escape(view["scope"])}</p>'

    return _section(
        "Question",
        f"""
        <p class="eyebrow">Experiment Narrative</p>
        <h1>{escape(view["title"])}</h1>
        {question_html}
        {scope_html}
        <div class="hero-meta">
          {_status_badge(view["status"])}
          <span class="chip">Generated: <code>{escape(view["generated_at"] or "unknown")}</code></span>
        </div>
        <div class="chips">
          {_methodology_chip("Baseline", view["methodology"].get("baseline_run_id"))}
          {_methodology_collection_chip("Variants", view["methodology"].get("variant_run_ids", []))}
          {_methodology_collection_chip("Comparisons", view["methodology"].get("comparison_ids", []))}
        </div>
        """,
    )


def _render_short_answer(view: dict[str, Any]) -> str:
    interpretation = view["interpretation"]
    if not interpretation:
        return ""
    executive_summary = interpretation.get("executive_summary", {})
    short_answer = str(executive_summary.get("short_answer", "")).strip()
    answer = str(executive_summary.get("answer", "")).strip()
    if not short_answer and not answer:
        return ""

    content = [
        '<p class="eyebrow">Answer</p>',
        "<h2>What Is the Answer?</h2>",
    ]
    if short_answer:
        content.append(f'<p class="lede">{escape(short_answer)}</p>')
    content.append(
        f"""
        <div class="story-grid">
          <div class="surface ink">
            <p>{escape(answer)}</p>
          </div>
          <div class="surface">
            <h3>Confidence</h3>
            <p>{_confidence_badge(executive_summary.get("confidence", ""))}</p>
            <h3>Evidence</h3>
            <div class="evidence-row">
              {_render_evidence_refs(executive_summary.get("evidence_refs", []), view["evidence_by_id"])}
            </div>
          </div>
        </div>
        """
    )
    return _section("Answer", "\n".join(content), panel_class="answer")


