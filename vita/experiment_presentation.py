def _baseline_objective(summary: dict) -> float | None:
    """Return the baseline run's objective value, if available."""
    meth = summary.get("methodology", {})
    bl_id = meth.get("baseline_run_id")
    if bl_id is None:
        return None
    for r in summary.get("runs", []):
        if r.get("id") == bl_id:
            return r.get("objective")
    return None


def _format_objective(val: Any) -> str:
    if val is None:
        return "—"
    if isinstance(val, (int, float)):
        return f"{val:,.4f}"
    return escape(str(val))


def _format_num(val: Any) -> str:
    if val is None:
        return "—"
    if isinstance(val, (int, float)):
        return f"{val:,.4f}"
    return escape(str(val))


def _format_delta(val: Any) -> str:
    if val is None:
        return '<span class="delta-zero">—</span>'
    if not isinstance(val, (int, float)):
        return escape(str(val))
    if abs(val) < 1e-9:
        return '<span class="delta-zero">0</span>'
    cls = "delta-pos" if val > 0 else "delta-neg"
    sign = "+" if val > 0 else ""
    return f'<span class="{cls}">{sign}{val:,.4f}</span>'


def _format_pct(val: Any) -> str:
    if val is None:
        return ""
    if isinstance(val, (int, float)):
        sign = "+" if val > 0 else ""
        return f"{sign}{val:.1f}%"
    return escape(str(val))


