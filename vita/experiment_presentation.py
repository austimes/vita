def _render_evidence_refs(refs: list[Any], evidence_by_id: dict[str, dict]) -> str:
    if not refs:
        return '<span class="chip evidence-chip">No evidence refs</span>'
    chips = []
    for ref in refs:
        ref_id = str(ref)
        evidence = evidence_by_id.get(ref_id, {})
        title_parts = [
            ref_id,
            str(evidence.get("kind", "")),
            str(evidence.get("metric", "")),
            str(evidence.get("source_file", "")),
        ]
        label = ref_id
        if evidence.get("metric"):
            label = f"{ref_id} · {evidence['metric']}"
        chips.append(
            f'<span class="chip evidence-chip" title="{escape(" | ".join(part for part in title_parts if part))}">{escape(label)}</span>'
        )
    return "".join(chips)


def _methodology_chip(label: str, value: Any) -> str:
    if not value:
        return ""
    return f'<span class="chip">{escape(label)}: <code>{escape(str(value))}</code></span>'


def _methodology_collection_chip(label: str, values: list[Any]) -> str:
    if not values:
        return ""
    joined = ", ".join(str(value) for value in values)
    return f'<span class="chip">{escape(label)}: <code>{escape(joined)}</code></span>'


def _status_badge(status: str) -> str:
    cls = {
        "concluded": "ok",
        "summarized": "warn",
        "complete": "warn",
        "running": "warn",
        "planned": "neutral",
    }.get(status, "neutral")
    return f'<span class="badge {cls}">{escape(status)}</span>'


def _confidence_badge(confidence: str) -> str:
    if not confidence:
        return '<span class="badge neutral">unknown confidence</span>'
    cls = {
        "high": "ok",
        "medium": "warn",
        "low": "danger",
    }.get(confidence.lower(), "neutral")
    return f'<span class="badge {cls}">{escape(confidence)}</span>'


def _hypothesis_badge(status: str) -> str:
    if not status:
        return '<span class="badge neutral">unknown assessment</span>'
    cls = {
        "supports": "ok",
        "refutes": "danger",
        "mixed": "warn",
        "inconclusive": "neutral",
    }.get(status.lower(), "neutral")
    return f'<span class="badge {cls}">{escape(status)}</span>'


def _direction_badge(direction: str) -> str:
    if not direction:
        return ""
    cls = {
        "increase": "warn",
        "decrease": "ok",
        "mixed": "warn",
        "no_change": "neutral",
        "uncertain": "neutral",
    }.get(direction.lower(), "neutral")
    return f'<span class="badge {cls}">{escape(direction)}</span>'


def _baseline_objective(summary: dict) -> float | None:
    baseline_run_id = summary.get("methodology", {}).get("baseline_run_id")
    if not baseline_run_id:
        return None
    for run in summary.get("runs", []):
        if run.get("id") == baseline_run_id:
            objective = run.get("objective")
            if isinstance(objective, (int, float)):
                return float(objective)
            return None
    return None


def _format_metric_name(value: Any) -> str:
    if not value:
        return "capacity"
    return str(value).replace("_", " ")


def _format_number(value: Any) -> str:
    if value is None:
        return "—"
    if isinstance(value, (int, float)):
        return f"{value:,.4f}"
    return str(value)


def _format_delta(value: Any) -> str:
    if value is None:
        return '<span class="delta zero">—</span>'
    if not isinstance(value, (int, float)):
        return escape(str(value))
    if abs(value) < 1e-9:
        return '<span class="delta zero">0.0000</span>'
    cls = "pos" if value > 0 else "neg"
    sign = "+" if value > 0 else ""
    return f'<span class="delta {cls}">{sign}{value:,.4f}</span>'


def _format_pct(value: Any) -> str:
    if value is None:
        return "—"
    if not isinstance(value, (int, float)):
        return str(value)
    sign = "+" if value > 0 else ""
    return f"{sign}{value:.1f}%"
