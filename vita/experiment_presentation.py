    comparisons = view["comparisons"]
    parts = ['<p class="eyebrow">Observed Results</p>', "<h2>What Happened</h2>"]
    parts.append(
        f"""
        <div class="kpi-row">
          <div class="kpi">
            <p class="kpi-label">Baseline Objective</p>
            <p class="kpi-value">{_format_number(view["baseline_objective"])}</p>
          </div>
          <div class="kpi">
            <p class="kpi-label">Largest Objective Delta</p>
            <p class="kpi-value">{_format_delta(view["largest_objective_delta"])}</p>
          </div>
          <div class="kpi">
            <p class="kpi-label">Runs</p>
            <p class="kpi-value">{view["run_count"]}</p>
          </div>
          <div class="kpi">
            <p class="kpi-label">Comparisons</p>
            <p class="kpi-value">{view["comparison_count"]}</p>
          </div>
        </div>
        """
    )
    if not comparisons:
        parts.append('<p class="empty-note">No comparison artifacts were available.</p>')
    for comparison in comparisons:
        largest_shift = comparison.get("largest_capacity_shift")
        shift_html = ""
        if largest_shift:
            shift_html = (
                '<div class="capacity-shift">'
                "<strong>Largest capacity shift:</strong> "
                f'<code>{escape(str(largest_shift.get("process", "")))}</code> '
                f"({_format_metric_name(largest_shift.get('metric'))}) "
                f"{_format_delta(largest_shift.get('delta'))}"
                "</div>"
            )

        parts.append(
            f"""
            <div class="comparison-card">
              <div class="metric-line">
                <strong>{escape(str(comparison.get("id", "")))}</strong>
                <span class="mono">{_format_delta(comparison.get("objective_delta"))}</span>
                <span class="mono">{escape(_format_pct(comparison.get("pct_objective_delta")))}</span>
              </div>
              <p class="lede">{escape(str(comparison.get("headline", "")))}</p>
              {_render_top_changes(comparison.get("top_changes", []))}
              {shift_html}
            </div>
            """
        )
    return _section("What Happened", "\n".join(parts))


