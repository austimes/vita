def _render_brief_section(brief: dict) -> str:
    """Render experiment design section from brief.json."""
    if not brief:
        return ""
    parts = ['<div class="brief-card"><h2>Experiment Design</h2>']

    approach = brief.get("design_approach", "")
    if approach:
        parts.append(f"<p>{escape(approach)}</p>")

    variants: list[dict] = brief.get("variants", [])
    for var in variants:
        vid = escape(str(var.get("id", var.get("name", ""))))
        parts.append(f"<h3>{vid}</h3>")

        change_summary = var.get("change_summary", "")
        if change_summary:
            parts.append(f"<p><strong>Change:</strong> {escape(change_summary)}</p>")

        hypothesis = var.get("hypothesis", "")
        if hypothesis:
            parts.append(f"<p><strong>Hypothesis:</strong> {escape(hypothesis)}</p>")

        mechanisms = var.get("expected_mechanisms", [])
        if mechanisms:
            parts.append("<p><strong>Expected mechanisms:</strong></p><ul>")
            for m in mechanisms:
                parts.append(f"<li>{escape(str(m))}</li>")
            parts.append("</ul>")

    parts.append("</div>")
    return "\n".join(parts)


def _render_interpretation_section(interpretation: dict) -> str:
    """Render agent interpretation section from interpretation.json."""
    if not interpretation:
        return ""
    parts = ['<div class="interpretation-card"><h2>Agent Interpretation</h2>']

    exec_summary = interpretation.get("executive_summary", "")
    overall_conf = interpretation.get("overall_confidence", "")
    if exec_summary:
        badge = _confidence_badge(overall_conf) if overall_conf else ""
        parts.append(f"<p>{escape(exec_summary)} {badge}</p>")

    comparisons: list[dict] = interpretation.get("comparisons", [])
    for comp in comparisons:
        cid = escape(str(comp.get("id", "")))
        parts.append(f"<h3>{cid}</h3>")

        takeaway = comp.get("takeaway", "")
        if takeaway:
            parts.append(f"<p><em>{escape(takeaway)}</em></p>")

        hyp_assessment = comp.get("hypothesis_assessment", "")
        if hyp_assessment:
            badge = _hypothesis_badge(hyp_assessment)
            parts.append(
                f"<p><strong>Hypothesis:</strong> {badge}</p>"
            )

        reasoning: list[dict] = comp.get("reasoning_chain", [])
        if reasoning:
            parts.append("<h4>Reasoning</h4>")
            for i, step in enumerate(reasoning, 1):
                step_type = step.get("type", "")
                step_text = escape(str(step.get("text", "")))
                css_cls = (
                    f"reasoning-step {step_type}"
                    if step_type
                    else "reasoning-step"
                )
                parts.append(
                    f'<div class="{css_cls}">'
                    f"<strong>{i}.</strong> {step_text}</div>"
                )

        mechanism = comp.get("primary_mechanism", "")
        if mechanism:
            parts.append(
                f"<p><strong>Primary mechanism:</strong> {escape(mechanism)}</p>"
            )

        surprises: list[str] = comp.get("surprises", [])
        if surprises:
            parts.append("<p><strong>Surprises:</strong></p><ul>")
            for s in surprises:
                parts.append(f"<li>{escape(str(s))}</li>")
            parts.append("</ul>")

    synthesis = interpretation.get("synthesis", "")
    if synthesis:
        parts.append(f"<h3>Synthesis</h3><p>{escape(synthesis)}</p>")

    parts.append("</div>")
    return "\n".join(parts)


def _render_question_answers(interpretation: dict) -> str:
    """Render research answers from interpretation.json."""
    if not interpretation:
        return ""
    qa_list: list[dict] = interpretation.get("question_answers", [])
    if not qa_list:
        return ""
    parts = ['<div class="card"><h2>Research Answers</h2>']
    for qa in qa_list:
        question = escape(str(qa.get("question", "")))
        short_answer = escape(str(qa.get("short_answer", "")))
        full_answer = escape(str(qa.get("answer", qa.get("full_answer", ""))))
        confidence = qa.get("confidence", "")
        evidence = qa.get("evidence_refs", [])

        badge = _confidence_badge(confidence) if confidence else ""
        parts.append(
            f'<div class="answer-block">'
            f"<h3>{question} {badge}</h3>"
            f'<div class="short-answer">{short_answer}</div>'
            f"<p>{full_answer}</p>"
        )
        if evidence:
            refs = ", ".join(escape(str(e)) for e in evidence)
            parts.append(f'<div class="evidence">Evidence: {refs}</div>')
        parts.append("</div>")
    parts.append("</div>")
    return "\n".join(parts)


def _confidence_badge(confidence: str) -> str:
    """Return a badge span for a confidence level."""
    cls = {
        "high": "badge-green",
        "medium": "badge-amber",
        "low": "badge-red",
    }.get(confidence.lower() if confidence else "", "badge-gray")
    return f'<span class="badge {cls}">{escape(confidence)}</span>'


