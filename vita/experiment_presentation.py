def _render_header(summary: dict) -> str:
    exp_id = escape(str(summary.get("experiment_id", "")))
    status = summary.get("status", "unknown")
    badge_cls = {
        "concluded": "badge-green",
        "complete": "badge-amber",
        "running": "badge-amber",
        "planned": "badge-gray",
    }.get(status, "badge-gray")
    ts = escape(str(summary.get("completed_at", "")))
    return f"""    <div class="hero">
      <div class="container">
        <h1>{exp_id}</h1>
        <span class="badge {badge_cls}">{escape(status)}</span>
        <div class="subtitle">{ts}</div>
      </div>
    </div>
    <div class="container">"""


def _render_question(summary: dict, interpretation: dict | None = None) -> str:
    answers = summary.get("answers", [])
    primary = [a for a in answers if a.get("kind") == "primary"]
    extensions = [a for a in answers if a.get("kind") == "extension"]
    if not primary and not extensions:
        return ""
    parts = ['<div class="card"><h2>Research Question</h2>']
    for p in primary:
        parts.append(f"<blockquote>{escape(p.get('question', ''))}</blockquote>")
    if interpretation:
        exec_summary = interpretation.get("executive_summary", "")
        if exec_summary:
            conf = interpretation.get("overall_confidence", "")
            badge = _confidence_badge(conf) if conf else ""
            parts.append(
                f'<p><strong>Summary:</strong> {escape(exec_summary)} {badge}</p>'
            )
    if extensions:
        parts.append("<h3>Extensions</h3><ul>")
        for ext in extensions:
            parts.append(f"<li>{escape(ext.get('question', ''))}</li>")
        parts.append("</ul>")
    parts.append("</div>")
    return "\n".join(parts)


def _render_methodology(summary: dict) -> str:
    meth = summary.get("methodology")
    if not meth:
        return ""
    baseline = escape(str(meth.get("baseline_run_id", "")))
    variants = meth.get("variant_run_ids", [])
    parts = [
        '<div class="card"><h2>Methodology</h2>',
        f"<p><strong>Baseline:</strong> <code>{baseline}</code></p>",
    ]
    if variants:
        parts.append("<p><strong>Variants:</strong> ")
        parts.append(", ".join(f"<code>{escape(v)}</code>" for v in variants))
        parts.append("</p>")

    comparisons = meth.get("comparison_ids", [])
    if comparisons:
        parts.append("<h3>Comparison Matrix</h3><ul>")
        for c in comparisons:
            parts.append(f"<li><code>{escape(c)}</code></li>")
        parts.append("</ul>")

    # Hypotheses from the summary comparisons
    hyps = [
        h
        for h in summary.get("hypothesis_outcomes", [])
        if h.get("hypothesis")
    ]
    if hyps:
        parts.append("<h3>Hypotheses</h3><ul>")
        for h in hyps:
            vid = escape(str(h.get("variant_id", "")))
            hyp = escape(str(h.get("hypothesis", "")))
            parts.append(f"<li><strong>{vid}:</strong> {hyp}</li>")
        parts.append("</ul>")

    parts.append("</div>")
    return "\n".join(parts)


def _render_results(summary: dict) -> str:
    runs: list[dict] = summary.get("runs", [])
    if not runs:
        return ""
    parts = [
        '<div class="card"><h2>Results Overview</h2>',
        "<table><thead><tr>"
        "<th>Run</th><th>Objective</th><th>Solver Status</th>"
        "</tr></thead><tbody>",
    ]
    baseline_obj = _baseline_objective(summary)
    for r in runs:
        rid = escape(str(r.get("id", "")))
        obj = r.get("objective")
        solver = escape(str(r.get("solver_status", "")))
        obj_str = _format_objective(obj)
        delta_html = ""
        if baseline_obj is not None and obj is not None:
            delta = obj - baseline_obj
            if abs(delta) > 1e-9:
                delta_html = f" ({_format_delta(delta)})"
        parts.append(
            f'<tr><td><code>{rid}</code></td>'
            f'<td class="mono">{obj_str}{delta_html}</td>'
            f"<td>{solver}</td></tr>"
        )
    parts.append("</tbody></table></div>")
    return "\n".join(parts)


def _render_comparisons(summary: dict) -> str:
    comps: list[dict] = summary.get("comparisons", [])
    if not comps:
        return ""
    parts = ['<div class="card"><h2>Comparison Details</h2>']
    for comp in comps:
        cid = escape(str(comp.get("id", "")))
        parts.append(f"<h3>{cid}</h3>")

        # Objective delta
        obj_delta = comp.get("objective_delta")
        pct_delta = comp.get("pct_objective_delta")
        headline = comp.get("headline", "")
        if obj_delta is not None:
            delta_html = _format_delta(obj_delta)
            pct_html = (
                f" ({_format_pct(pct_delta)})" if pct_delta is not None else ""
            )
            parts.append(
                f'<p><strong>Objective Δ:</strong> <span class="mono">'
                f"{delta_html}{pct_html}</span></p>"
            )
        if headline:
            parts.append(f"<p><em>{escape(headline)}</em></p>")

        # Top changes
        top_changes: list[Any] = comp.get("top_changes", [])
        if top_changes:
            parts.append("<h4>Top Changes</h4><ul>")
            for tc in top_changes:
                if isinstance(tc, str):
                    parts.append(f"<li>{escape(tc)}</li>")
                elif isinstance(tc, dict):
                    parts.append(f"<li>{escape(str(tc))}</li>")
            parts.append("</ul>")

        # Capacity deltas
        cap_deltas: list[dict] = comp.get("capacity_deltas", [])
        if cap_deltas:
            parts.append(
                "<h4>Capacity Changes</h4>"
                "<table><thead><tr>"
                "<th>Process</th><th>Baseline</th><th>Variant</th><th>Δ</th>"
                "</tr></thead><tbody>"
            )
            for cd in cap_deltas:
                proc = escape(str(cd.get("process", "")))
                bval = _format_num(cd.get("baseline"))
                vval = _format_num(cd.get("variant"))
                dval = cd.get("delta")
                parts.append(
                    f'<tr><td><code>{proc}</code></td>'
                    f'<td class="mono">{bval}</td>'
                    f'<td class="mono">{vval}</td>'
                    f'<td class="mono">{_format_delta(dval)}</td></tr>'
                )
            parts.append("</tbody></table>")

    parts.append("</div>")
    return "\n".join(parts)


def _render_answers(summary: dict) -> str:
    answers: list[dict] = summary.get("answers", [])
    if not answers:
        return ""
    parts = ['<div class="card"><h2>Answers</h2>']
    for ans in answers:
        qid = escape(str(ans.get("question_id", "")))
        question = escape(str(ans.get("question", "")))
        status = ans.get("status", "")
        short_answer = escape(str(ans.get("short_answer", "")))
        answer = escape(str(ans.get("answer", "")))
        evidence = ans.get("evidence_refs", [])

        badge_cls = _answer_badge_class(status)
        parts.append(
            f'<div class="answer-block">'
            f"<h3>{qid}: {question} "
            f'<span class="badge {badge_cls}">{escape(status)}</span></h3>'
            f'<div class="short-answer">{short_answer}</div>'
            f"<p>{answer}</p>"
        )
        if evidence:
            refs = ", ".join(escape(str(e)) for e in evidence)
            parts.append(f'<div class="evidence">Evidence: {refs}</div>')
        parts.append("</div>")
    parts.append("</div>")
    return "\n".join(parts)


def _render_findings(summary: dict) -> str:
    findings: list[dict] = summary.get("key_findings", [])
    if not findings:
        return ""
    parts = ['<div class="card"><h2>Key Findings</h2>']
    for f in findings:
        fid = escape(str(f.get("id", "")))
        stmt = escape(str(f.get("statement", "")))
        evidence = f.get("evidence_refs", [])
        refs = ""
        if evidence:
            refs_str = ", ".join(escape(str(e)) for e in evidence)
            refs = f' <span class="evidence">[{refs_str}]</span>'
        parts.append(
            f'<div class="finding"><span class="fid">{fid}.</span> {stmt}{refs}</div>'
        )
    parts.append("</div>")
    return "\n".join(parts)


def _render_hypothesis_outcomes(summary: dict) -> str:
    hyps: list[dict] = summary.get("hypothesis_outcomes", [])
    if not hyps:
        return ""
    parts = [
        '<div class="card"><h2>Hypothesis Outcomes</h2>',
        "<table><thead><tr>"
        "<th>Variant</th><th>Hypothesis</th><th>Outcome</th>"
        "</tr></thead><tbody>",
    ]
    for h in hyps:
        vid = escape(str(h.get("variant_id", "")))
        hyp = escape(str(h.get("hypothesis", "")))
        outcome = h.get("outcome", "")
        badge_cls = _outcome_badge_class(outcome)
        parts.append(
            f"<tr><td><code>{vid}</code></td>"
            f"<td>{hyp}</td>"
            f'<td><span class="badge {badge_cls}">{escape(outcome)}</span></td></tr>'
        )
    parts.append("</tbody></table></div>")
    return "\n".join(parts)


def _render_surprises(summary: dict) -> str:
    surprises: list[dict] = summary.get(
        "surprises", summary.get("candidate_anomalies", [])
    )
    if not surprises:
        return ""
    parts = ['<div class="card"><h2>Surprises</h2>']
    for s in surprises:
        stmt = escape(str(s.get("statement", "")))
        follow_up = s.get("follow_up", "")
        evidence = s.get("evidence_refs", [])
        parts.append(f'<div class="surprise"><p>{stmt}</p>')
        if follow_up:
            parts.append(
                f"<p><strong>Follow-up:</strong> {escape(str(follow_up))}</p>"
            )
        if evidence:
            refs = ", ".join(escape(str(e)) for e in evidence)
            parts.append(f'<span class="evidence">Evidence: {refs}</span>')
        parts.append("</div>")
    parts.append("</div>")
    return "\n".join(parts)


def _render_limitations(summary: dict) -> str:
    limitations: list[str] = summary.get("limitations", [])
    if not limitations:
        return ""
    parts = [
        '<div class="card"><h2>Limitations</h2>',
        '<ul class="limitations">',
    ]
    for lim in limitations:
        parts.append(f"<li>{escape(str(lim))}</li>")
    parts.append("</ul></div>")
    # Close the container opened in _render_header
    parts.append("</div>")
    return "\n".join(parts)


